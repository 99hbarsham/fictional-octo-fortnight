select category_name
from genres
WHERE category_name NOT LIKE ('%Comedy%') 
AND category_name NOT LIKE ('%Animation%') 
AND category_name NOT LIKE ('%Action%') 
AND category_name NOT LIKE ('%Adventure%')
AND category_name NOT LIKE ('%Drama%')
AND category_name NOT LIKE ('%War%')
AND category_name NOT LIKE ('%Documentary%')
AND category_name NOT LIKE ('%Mystery%')
AND category_name NOT LIKE ('%Horror%')
AND category_name NOT LIKE ('%Thriller%')
AND category_name NOT LIKE ('%Family%')
AND category_name NOT LIKE ('%Fantasy%')
AND category_name NOT LIKE ('%Science%')


